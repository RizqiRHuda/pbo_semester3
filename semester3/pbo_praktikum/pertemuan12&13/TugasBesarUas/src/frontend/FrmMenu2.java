    public void tampilkanData() {
       Object [] namaKolom = new Object [] {"idMenu", "namaMenu", "harga", "tambahan"};
          
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
       
        ArrayList<Menu> listMenu = new Menu().getAll();
        Object rowData[] = new Object[4];

        for (Menu mn : listMenu) {
            rowData[0] = mn.getIdMenu();
            rowData[1] = mn.getNamaMenu();
            rowData[2] = mn.getHarga();
            rowData[3] = mn.getTambahan();

            model.addRow(rowData);
        }
        
        tblMenuMakanan.setModel(model);
    }
    
     public void cari(String keyword){
        Object[] namaKolom = {"IdMenu", "NamaMenu", "harga", "tambahan"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Menu> listMenu = Menu.search(keyword);
        Object rowData[] = new Object[4];
       
        
        for(Menu mn : listMenu)
        {
            rowData[0] = mn.getIdMenu();
            rowData[1] = mn.getNamaMenu();
            rowData[2] = mn.getHarga();
            rowData[3] = mn.getTambahan();
            
           model.addRow(rowData);
        }
        
        tblMenuMakanan.setModel(model);
    }

