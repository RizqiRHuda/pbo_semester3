    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        String namaMenu = txtNamaMenu.getText();
        String harga = txtHarga.getText();
        String tambahan = txtTambahan.getText();

        if (!(namaMenu.isEmpty() && harga.isEmpty() && tambahan.isEmpty())) {
            Menu mn = new Menu();
            mn.setIdMenu(Integer.parseInt(txtIdMenu.getText()));
            mn.setNamaMenu(namaMenu);
            mn.setHarga(Integer.parseInt(txtHarga.getText()));
            mn.setTambahan(tambahan);
            mn.save();
            
            tampilkanData();
        } else {
            JOptionPane.showMessageDialog(null, "Silahkan isi Nama, Telepon, dan Alamat!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblMenuMakananMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuMakananMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblMenuMakanan.getModel();
        int row =  tblMenuMakanan.getSelectedRow();

        txtIdMenu.setText(model.getValueAt(row, 0).toString());
        txtNamaMenu.setText(model.getValueAt(row, 1).toString());
        txtHarga.setText(model.getValueAt(row, 2).toString());
        txtTambahan.setText(model.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblMenuMakananMouseClicked

