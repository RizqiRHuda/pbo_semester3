    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String idAnggota = txtIdAnggota.getText();
        String idBuku = txtIdBuku.getText();
        String tanggalPinjam = txtTanggalPinjam.getText();
        String tanggalKembali = txtTanggalKembali.getText();

        if (!(idAnggota.isEmpty() && idBuku.isEmpty() && tanggalPinjam.isEmpty() && tanggalKembali.isEmpty())) {
            Peminjaman pjm = new Peminjaman();
            pjm.setIdPeminjaman(Integer.parseInt(txtIdPeminjaman.getText()));
            pjm.setIdAnggota(Integer.parseInt(idAnggota));
            pjm.setIdBuku(Integer.parseInt(idBuku));
            pjm.setTanggalPinjam(tanggalPinjam);
            pjm.setTanggalKembali(tanggalKembali);
            pjm.save();

            tampilkanData();
        } else {
            JOptionPane.showMessageDialog(null, "Silahkan Isi Form dengan Lengkap!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

