    public void tampilkanData() {
        Object[] namaKolom = new Object[]{"ID", "Peminjam", "Buku", "Pinjam", "Kembali"};

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);

        ArrayList<Peminjaman> listPeminjaman = Peminjaman.getAll();
        Object[] rowData = new Object[5];

        for (Peminjaman pjm : listPeminjaman) {
            rowData[0] = pjm.getIdPeminjaman();
            rowData[1] = cariNamaAnggota(String.valueOf(pjm.getIdAnggota()));
            rowData[2] = cariJudulBuku(Integer.toString(pjm.getIdBuku()));
            try {
                rowData[3] = ndf.format(df.parse(pjm.getTanggalPinjam()));
                rowData[4] = ndf.format(df.parse(pjm.getTanggalKembali()));
            } catch (Exception e) {
                rowData[3] = "Unknown Date";
                rowData[4] = "Unknown Date";
                e.printStackTrace();
            }

            model.addRow(rowData);
        }

        tblPeminjaman.setModel(model);
    }

