    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblPeminjaman.getModel();
        int rowIndex = tblPeminjaman.getSelectedRow();
        int idPeminjaman = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());

        Peminjaman pjm = Peminjaman.getById(idPeminjaman);

        txtIdPeminjaman.setText(Integer.toString(idPeminjaman));
        txtIdAnggota.setText(Integer.toString(pjm.getIdAnggota()));
        txtIdBuku.setText(Integer.toString(pjm.getIdBuku()));
        txtTanggalPinjam.setText(pjm.getTanggalPinjam());
        txtTanggalKembali.setText(pjm.getTanggalKembali());
    }//GEN-LAST:event_tblPeminjamanMouseClicked

