    public String cariNamaAnggota(String keyword) {
        String query = "SELECT nama FROM anggota WHERE idanggota = " + keyword;
        ResultSet rs = DBHelper.selectQuery(query);

        try {
            while (rs.next()) {
                return rs.getString("nama");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return "Not Found";
    }

    public String cariJudulBuku(String keyword) {
        String query = "SELECT judul FROM buku WHERE idbuku = " + keyword;
        ResultSet rs = DBHelper.selectQuery(query);

        try {
            while (rs.next()) {
                return rs.getString("judul");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return "Not Found";
    }

